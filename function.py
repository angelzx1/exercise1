def summ(a, b):
    return a + b

def sub(c, d):
    return c - d

def mult(e, f):
    return e * f

def div(g, h):
    return g/h

#examples
print("the value of the sum is " + str(summ(7,5)))
print("the value of the subtraction is " + str(sub(10, 3)))
print("the value of the multiplication is " + str(mult(8, 4)))
print("the value of the division is " + str(div(10, 2)))
